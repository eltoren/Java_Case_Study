-- Oracle  tables for ABIS courses (sqlfun/sqlws/orabas/plsql/sqladv)

  CREATE TABLE COMPANIES
    (CONO      number       primary key,
     CONAME    CHAR    (45) NOT NULL,
     COSTREET  VARCHAR (45) NOT NULL,
     COSTRNO   VARCHAR (10),
     COTOWN    CHAR    (45) NOT NULL,
     COTOWNNO  CHAR    (10),
     COCOUNTR  CHAR     (4),
     COTEL     CHAR    (16),
     COVAT     CHAR    (11),
     COBANKNO  CHAR    (14),
     COC_PNO   number
    )
   ;
--
  CREATE TABLE PERSONS
    (PNO       number  primary key,
     PLNAME    CHAR    (40) NOT NULL,
     PFNAME    VARCHAR (15),
     PFUNC     CHAR    (20),
     PA_CONO   number       constraint fk_pa_cono references companies ON DELETE SET NULL,
     PADEPT    CHAR    (30),
     PTEL      CHAR    (16),
     PSEX      CHAR     (1)
    )
   ;
--
  CREATE TABLE COURSES
    (CID       CHAR     (4) primary key,
     CSTITLE   CHAR    (45) NOT NULL,
     CLTITLE   VARCHAR (60),
     CDUR      number       NOT NULL,
     CAPRICE   number (9,2) NOT NULL
    )
   ;
--
  CREATE TABLE SESSIONS
    (SNO       number       primary key,
     SDATE     DATE         NOT NULL,
     SINS_PNO  number       NOT NULL constraint fk_sins_pno references  persons,
     SLOC_CONO number       NOT NULL constraint fk_sloc_cono references companies,
     SROOM     CHAR     (8) DEFAULT ' ' NOT NULL,
     SORG_CONO number                constraint fk_sorg_cono references companies,
     SKIND     CHAR     (1) NOT NULL,
     SINCOMES  number (9,2) NOT NULL,
     SCANCEL   CHAR     (1),
     S_CID     CHAR     (4) NOT NULL constraint fk_s_cid references courses
    )
   ;
--
  CREATE TABLE ENROLMENTS
    (E_SNO     number       NOT NULL constraint fk_e_sno references sessions ON DELETE CASCADE,
     ENO       number       NOT NULL,
     E_PNO     number              constraint fk_e_pno references persons,
     EPAY      number (9,2) NOT NULL,
     E_CONO    number       NOT NULL constraint fk_e_cono references companies,
     ECANCEL   CHAR     (1),
     EINV_CONO number       NOT NULL constraint fk_einv_cono references companies,
     constraint pk_eno_e_sno primary key(eno,e_sno)
    )
   ;
